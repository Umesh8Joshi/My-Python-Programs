'''
The following class will be used as the calculator
Class will have different methods to perform simple expressions
'''

class Calc():
	def __init__(a,b,self):
		self.a = a
		self.b = b
	def sum(a,b,self):
		return self.a + self.b
	def min(a,b,self):
		return self.a - self.b
	def mul(a,b,self):
		return self.a*self.b
	def div(a,b,self):
		return self.a / self.b
